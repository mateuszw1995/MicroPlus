#pragma once

#include "../../Defines.h"
#include "../../Graphics/Color.h"

/**********************************************************************************************************************************************/
RGBA_256::RGBA_256(unsigned char rr, unsigned char gg, unsigned char bb, unsigned char aa)
	: r(rr), g(gg), b(bb), alpha(aa) {}

/**********************************************************************************************************************************************/
RGBA_1::RGBA_1(GLfloat rr, GLfloat gg, GLfloat bb, GLfloat aa)
	: r(rr), g(gg), b(bb), alpha(aa)  {}

/**********************************************************************************************************************************************/
RGBA_1::RGBA_1(RGBA_256 aa)
	: r(aa.r/255.0),g(aa.g/255.0),b(aa.b/255.0),alpha(aa.alpha/255.0){}

/**********************************************************************************************************************************************/
int RGBA_1::operator!=(RGBA_1 c)
{
	return(r != c.r || g != c.g || b != c.b || alpha != c.alpha);
}

/**********************************************************************************************************************************************/
RGBA_1::operator RGBA_256()
{
	RGBA_256 a(r*255, g*255, b*255, alpha*255);
	return a;
}

/**********************************************************************************************************************************************/
int RGBA_1::operator!=(RGBA_256 d)
{
	RGBA_1 c = RGBA_1(d.r/255.0, d.g/255.0, d.b/255.0, d.alpha/255.0);
	return(r != c.r || g != c.g || b != c.b || alpha != c.alpha);
}
/**********************************************************************************************************************************************/

RGBA_1::operator vec4() {
	vec4 v = { r,g,b,alpha };
	return v;
}